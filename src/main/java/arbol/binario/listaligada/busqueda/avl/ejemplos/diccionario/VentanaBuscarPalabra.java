    public void PedirDatos_llenarCampo(){
        
        if(TextArea_Significado.getText().equals("")) 
            TextArea_Significado.setText("Indique el significado de la palabra");
        if(TextArea_Sinonimos.getText().equals("")) 
            TextArea_Sinonimos.setText("Indique el o los sinonimos de la palabra");
        if(TextArea_Antonimos.getText().equals("")) 
        TextArea_Antonimos.setText("Indique el o los antonimos de la palabra");
    }
    public void LimpiarCampo(){
        TextArea_Significado.setText("");
        TextArea_Sinonimos.setText("");
        TextArea_Antonimos.setText("");
        TextArea_Resultado.setText("");
        TextArea_Palabra.setText("");
    }

    
